let cart = {};

function init() {
    const list = document.getElementById('category-list');
    if(list) {
        list.innerHTML = Object.keys(storeData).map(cat => `
            <div class="cat-card" onclick="openCategory('${cat}')">
                <img src="${storeData[cat].icon}" alt="${cat}">
                <p>${cat}</p>
            </div>
        `).join('');
    }
}

function openCategory(name) {
    document.getElementById('category-view').classList.add('hidden');
    document.getElementById('product-view').classList.remove('hidden');
    document.getElementById('category-title').innerText = name;
    renderProducts(name);
}

function goBack() {
    document.getElementById('category-view').classList.remove('hidden');
    document.getElementById('product-view').classList.add('hidden');
}

function renderProducts(name) {
    const list = document.getElementById('product-list');
    const items = storeData[name].items || [];
    list.innerHTML = items.map(p => {
        const q = cart[p.id] || 0;
        return `
            <div class="product-card">
                <img src="${p.img}" alt="${p.name}">
                <div class="product-name">${p.name}</div>
                <div style="font-size:22px; margin-bottom:15px;"><strong>₹${p.price}</strong></div>
                <div id="ctrl-${p.id}">
                    ${q > 0 ? getQtyHTML(p.id, q) : `<button class="btn-add" onclick="updateCart(${p.id}, 1)">ADD</button>`}
                </div>
            </div>`;
    }).join('');
}

function getQtyHTML(id, q) {
    return `<div class="qty-controls">
        <button class="qty-btn" onclick="updateCart(${id}, -1)">-</button>
        <span style="font-size:20px; font-weight:bold;">${q}</span>
        <button class="qty-btn" onclick="updateCart(${id}, 1)">+</button>
    </div>`;
}

function updateCart(id, change) {
    cart[id] = (cart[id] || 0) + change;
    if (cart[id] <= 0) delete cart[id];
    
    const ctrl = document.getElementById(`ctrl-${id}`);
    if (ctrl) {
        const q = cart[id] || 0;
        ctrl.innerHTML = q > 0 ? getQtyHTML(id, q) : `<button class="btn-add" onclick="updateCart(${id}, 1)">ADD</button>`;
    }
    updateCartUI();
}

function updateCartUI() {
    let count = 0, total = 0, html = "";
    for (let id in cart) {
        const p = findP(id);
        if (p) {
            count += cart[id];
            total += p.price * cart[id];
            html += `
                <div class="cart-item">
                    <div><b>${p.name}</b><br><small>₹${p.price} x ${cart[id]}</small></div>
                    <b>₹${p.price * cart[id]}</b>
                </div>`;
        }
    }
    document.getElementById('top-amount').innerText = total;
    document.getElementById('modal-total').innerText = total;
    document.getElementById('cart-items-list').innerHTML = html || "Cart is empty";
    document.getElementById('cart-count').innerText = count;
    document.getElementById('cart-bar').classList.toggle('hidden', count === 0);
}

function findP(id) {
    for (let c in storeData) {
        let p = storeData[c].items.find(i => i.id == id);
        if (p) return p;
    }
    return null;
}

function toggleModal(show) {
    const modal = document.getElementById('cart-modal');
    if (show) modal.classList.remove('hidden');
    else modal.classList.add('hidden');
}

