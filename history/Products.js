function handleOrder() {
    if (Object.keys(cart).length === 0) {
        alert("Your cart is empty!");
        return;
    }
    toggleModal(false); 
    document.getElementById('user-detail-modal').classList.remove('hidden'); 
}

function closeUserModal() {
    document.getElementById('user-detail-modal').classList.add('hidden');
}

function finalizeOrder(event) {
    event.preventDefault();

    const userData = {
        name: document.getElementById('u-name').value,
        phone: document.getElementById('u-phone').value,
        area: document.getElementById('u-area').value,
        address: document.getElementById('u-address').value
    };

    const orderDetails = {
        orderId: "#ORD" + Math.floor(Math.random() * 100000),
        date: new Date().toLocaleString('en-IN'), // Changed to English-India format
        customer: userData,
        items: [],
        total: document.getElementById('modal-total').innerText
    };

    for (let id in cart) {
        const p = findP(id);
        if (p) {
            orderDetails.items.push({
                name: p.name,
                qty: cart[id],
                price: p.price * cart[id]
            });
        }
    }

    let allOrders = JSON.parse(localStorage.getItem('myOrders')) || [];
    allOrders.push(orderDetails);
    localStorage.setItem('myOrders', JSON.stringify(allOrders));

    alert("Thank you " + userData.name + "! Your order has been placed successfully.");
    
    cart = {};
    updateCartUI();
    closeUserModal();
    window.location.href = "orders.html";
}

